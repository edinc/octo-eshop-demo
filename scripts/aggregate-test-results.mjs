#!/usr/bin/env node
/**
 * Aggregates per-workspace vitest JSON results and coverage summaries
 * from .artifacts/tests/<workspace>/ and writes a markdown PR comment
 * to .artifacts/test-summary.md.
 *
 * Expected inputs (produced by `test:ci` scripts):
 *   .artifacts/tests/<workspace>/results.json          – vitest JSON reporter output
 *   .artifacts/tests/<workspace>/coverage/coverage-summary.json – @vitest/coverage-v8 JSON
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');
const artifactsDir = join(repoRoot, '.artifacts', 'tests');

const WORKSPACES = [
  'user-service',
  'product-service',
  'cart-service',
  'order-service',
  'payment-service',
  'frontend',
];

function readJson(filePath) {
  if (!existsSync(filePath)) return null;
  try {
    return JSON.parse(readFileSync(filePath, 'utf8'));
  } catch {
    return null;
  }
}

function pct(value) {
  if (value === null || value === undefined) return 'N/A';
  return `${Number(value).toFixed(1)}%`;
}

function durationSec(ms) {
  if (ms === null || ms === undefined || ms < 0) return 'N/A';
  return (ms / 1000).toFixed(1);
}

const rows = [];
let totalTests = 0;
let totalPassed = 0;
let totalFailed = 0;
let totalSkipped = 0;
let totalDurationMs = 0;
let coveredLines = 0;
let totalLines = 0;
let allPassed = true;

for (const workspace of WORKSPACES) {
  const resultsPath = join(artifactsDir, workspace, 'results.json');
  const coveragePath = join(artifactsDir, workspace, 'coverage', 'coverage-summary.json');

  const results = readJson(resultsPath);
  const coverage = readJson(coveragePath);

  if (!results) {
    rows.push({
      workspace,
      tests: '–',
      passed: '–',
      failed: '–',
      skipped: '–',
      duration: '–',
      coverage: '–',
      missing: true,
    });
    allPassed = false;
    continue;
  }

  const tests = results.numTotalTests ?? 0;
  const passed = results.numPassedTests ?? 0;
  const failed = results.numFailedTests ?? 0;
  // Pending, skipped, and todo tests are all non-executed tests; group under "Skipped" for display.
  const skipped =
    (results.numPendingTests ?? 0) + (results.numSkippedTests ?? 0) + (results.numTodoTests ?? 0);

  // Compute duration from startTime to max testResult end (or use reported duration)
  let durationMs = 0;
  if (Array.isArray(results.testResults) && results.testResults.length > 0) {
    const start = results.startTime ?? results.testResults[0]?.startTime ?? 0;
    const end = Math.max(...results.testResults.map(r => r.endTime ?? r.startTime ?? 0));
    durationMs = end > start ? end - start : 0;
  }

  const linePct = coverage?.total?.lines?.pct ?? null;

  totalTests += tests;
  totalPassed += passed;
  totalFailed += failed;
  totalSkipped += skipped;
  totalDurationMs += durationMs;

  if (linePct !== null) {
    coveredLines += coverage.total.lines.covered ?? 0;
    totalLines += coverage.total.lines.total ?? 0;
  }

  if (failed > 0 || !results.success) allPassed = false;

  rows.push({
    workspace,
    tests,
    passed,
    failed,
    skipped,
    duration: durationSec(durationMs),
    coverage: pct(linePct),
    missing: false,
  });
}

const overallCoverage = totalLines > 0 ? pct((coveredLines / totalLines) * 100) : 'N/A';
const statusEmoji = allPassed ? '✅' : '❌';
const statusText = allPassed ? 'All tests passed' : 'Some tests failed';

const tableRows = rows
  .map(r => {
    const failedCell = r.failed !== '–' && r.failed > 0 ? `**${r.failed}**` : r.failed;
    const missingNote = r.missing ? ' ⚠️ report missing' : '';
    return `| ${r.workspace}${missingNote} | ${r.tests} | ${r.passed} | ${failedCell} | ${r.skipped} | ${r.duration} | ${r.coverage} |`;
  })
  .join('\n');

const markdown = `## ${statusEmoji} CI Test Summary

> ${statusText}

- **Total tests:** ${totalTests}
- **Overall line coverage:** ${overallCoverage}

| Workspace | Tests | Passed | Failed | Skipped | Duration (s) | Line Coverage |
|---|---:|---:|---:|---:|---:|---:|
${tableRows}

<sub>Generated by CI · ${new Date().toUTCString()}</sub>
<!-- ci-test-summary -->`;

const outputDir = join(repoRoot, '.artifacts');
mkdirSync(outputDir, { recursive: true });
const outputPath = join(outputDir, 'test-summary.md');
writeFileSync(outputPath, markdown, 'utf8');

// Also print to stdout for debugging
console.log(markdown);
console.log(`\nWrote summary to ${outputPath}`);
